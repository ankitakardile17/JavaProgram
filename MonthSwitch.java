package myproje;

public class MonthSwitch {

	public static void main(String[] args) {
		String str="March";
		switch(str)
		{
		case "Jan":
			System.out.println(31);
			break;
			
		case "Feb":
			System.out.println(28/29);
			break;
			
		case "March":
			System.out.println(31);
			break;
			
		case "April":
			System.out.println(30);
			break;
			
		case "June":
			System.out.println(31);
			break;
			
		case "July":
			System.out.println(30);
			break;
			
		case "August":
			System.out.println(31);
			break;
			
		case "sept":
			System.out.println(30);
			break;
			
		case "oct":
			System.out.println(31);
			break;
			
		case "nov":
			System.out.println(30);
			break;
			
		case "dec":
			System.out.println(31);
			break;
			
		
		}

	}

}
